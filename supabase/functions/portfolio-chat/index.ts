import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

const SYSTEM_PROMPT = `You are Elbara's AI assistant on his portfolio website. You help visitors learn about Elbara Mouaffak, an AI Engineering student at University of Batna 2, Algeria.

Key information about Elbara:
- AI Engineering student (since Sep 2021)
- Freelance AI & Automation Developer (since Jan 2024)
- Skills: Python, TensorFlow, PyTorch, React, TypeScript, n8n, Supabase, LangChain, OpenAI APIs
- Built AI-powered automation solutions reducing manual work by 80%
- Developed 5+ production full-stack web applications
- Created custom chatbots and RAG systems
- Active in developer communities, won recognition at DevFest 2025 and DevsForDevs 2024
- Open source contributor with projects having 100+ GitHub stars
- Located in Batna, Algeria

Projects:
- Fessity: E-commerce dashboard with React, TypeScript, Supabase
- Hospital Management System: Patient records and appointments
- AI Chatbot: Customer support automation with OpenAI
- Automations & Workflows: n8n automation workflows
- Satellite Analysis: Environmental analysis with Google Earth Engine

Keep responses concise, friendly, and professional. If asked about hiring or collaboration, encourage visitors to use the contact page. If you don't know something specific, say so honestly.`;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    console.log("Received chat request with", messages.length, "messages");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: SYSTEM_PROMPT },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        console.error("Rate limit exceeded");
        return new Response(JSON.stringify({ error: "Too many requests. Please try again later." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        console.error("Payment required");
        return new Response(JSON.stringify({ error: "Service temporarily unavailable." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      return new Response(JSON.stringify({ error: "Failed to get AI response" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    console.log("Streaming response from AI gateway");
    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("Chat error:", error);
    return new Response(JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
